function color() {
    document.body.style.background = `linear-gradient(to right, rgb(${Math.floor(Math.random() * 255)},
     ${Math.floor(Math.random() * 255)},
      ${Math.floor(Math.random() * 255)}),
    rgb(${Math.floor(Math.random() * 255)},
     ${Math.floor(Math.random() * 255)},
      ${Math.floor(Math.random() * 255)}))`
}